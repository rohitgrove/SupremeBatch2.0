public class InPlaceMergeSort {
    public static void main(String[] args) {
        int arr[] = { 1, 2, 8, 3, 9, 4, 12, 7, 13, 10 };
        
    }
    public static void printArr(int arr[]) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
        System.out.println();
    }
}
